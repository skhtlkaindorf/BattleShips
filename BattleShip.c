#include<stdio.h>

void print(int *map, int length) {
    for (int i = 0; i < length; i++) {
        if (map[i] != 0)
            printf("%+d\t", map[i]);
        else
            printf(" %d\t", map[i]);

        if ((i + 1) % 10 == 0) {
            printf("\n");
        }
    }
}

